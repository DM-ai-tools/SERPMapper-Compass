async function seed() {
  console.log(`\nSeeding ${SUBURBS.length} suburbs across all Australian states...\n`);

  let inserted = 0;
  let skipped  = 0;

  for (const [name, state, postcode, lat, lng] of SUBURBS) {
    try {
      const res = await pool.query(
        `INSERT INTO suburb_coordinates (name, state, postcode, lat, lng)
         VALUES ($1, $2, $3, $4, $5)
         ON CONFLICT (name, state) DO NOTHING
         RETURNING suburb_id`,
        [name, state, postcode, lat, lng]
      );
      if (res.rowCount > 0) { inserted++; process.stdout.write('.'); }
      else                  { skipped++;  process.stdout.write('s'); }
    } catch (e) {
      console.error(`\n  ERROR for ${name} (${state}):`, e.message);
    }
  }

  console.log(`\n\nDone! Inserted: ${inserted}, Already existed: ${skipped}\n`);

  // Summary
  const byState = await pool.query(
    'SELECT state, COUNT(*) as count FROM suburb_coordinates GROUP BY state ORDER BY state'
  );
  console.log('Final suburb counts by state:');
  let total = 0;
  byState.rows.forEach(r => {
    console.log(`  ${r.state}: ${r.count}`);
    total += parseInt(r.count);
  });
  console.log(`  TOTAL: ${total}`);
}

seed().catch(console.error).finally(() => pool.end());
